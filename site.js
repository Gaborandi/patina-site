const FALLBACK_LANG = 'en';
const RTL_LANGS = new Set(['ar']);
const STORAGE_KEY = 'patina-site-language';

function nestedValue(source, path) {
  return path.split('.').reduce((obj, key) => {
    if (!obj || !Object.prototype.hasOwnProperty.call(obj, key)) return null;
    return obj[key];
  }, source);
}

function readSavedLanguage() {
  try {
    return window.localStorage.getItem(STORAGE_KEY);
  } catch {
    return null;
  }
}

function saveLanguage(lang) {
  try {
    window.localStorage.setItem(STORAGE_KEY, lang);
  } catch {
    // Ignore private-mode storage failures. The page still works.
  }
}

function requestedLanguage() {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get('lang');
  if (fromUrl && PATINA_I18N[fromUrl]) return fromUrl;

  const saved = readSavedLanguage();
  if (saved && PATINA_I18N[saved]) return saved;

  const browserLang = (navigator.language || '').slice(0, 2);
  return PATINA_I18N[browserLang] ? browserLang : FALLBACK_LANG;
}

function applyLanguage(lang) {
  const dictionary = PATINA_I18N[lang] || PATINA_I18N[FALLBACK_LANG];

  document.documentElement.lang = lang;
  document.body.dir = RTL_LANGS.has(lang) ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    const value = nestedValue(dictionary, key) || nestedValue(PATINA_I18N[FALLBACK_LANG], key);
    if (value) node.textContent = value;
  });

  document.querySelectorAll('[data-language-select]').forEach((select) => {
    select.value = lang;
  });

  const titleKey = document.body.getAttribute('data-title-key');
  const title = titleKey ? nestedValue(dictionary, titleKey) || nestedValue(PATINA_I18N[FALLBACK_LANG], titleKey) : null;
  if (title) document.title = title;

  saveLanguage(lang);
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(requestedLanguage());

  document.querySelectorAll('[data-language-select]').forEach((select) => {
    select.addEventListener('change', (event) => {
      applyLanguage(event.target.value);
    });
  });
});
